import shelve

def manage_expenses():
    """
    Программа для учёта расходов.
    Сохраняет общую сумму потраченных рублей в базу данных 'expenses.db'.
    """
    # Открываем (или создаем, если её нет) базу данных с именем 'expenses'.
    # Конструкция 'with' гарантирует, что база данных автоматически и правильно
    # закроется (.close()), даже если программа завершится аварийно.
    with shelve.open('expenses') as db:
        
        # Условие задачи: При первом запуске программы текущие расходы равны 0.
        # Проверяем, есть ли уже в нашей базе данных ключ 'total' (общая сумма).
        if 'total' not in db:
            db['total'] = 0  # Если ключа нет (первый запуск), записываем ноль
            
        # Выводим текущее состояние расходов сразу при старте программы
        print(f"Текущие расходы: {db['total']} руб.")

        # Запускаем бесконечный цикл для постоянного запроса новых расходов
        while True:
            try:
                # Запрашиваем у пользователя сумму. Метод input() всегда возвращает строку.
                user_input = input("Введите потраченную сумму в рублях (или 0 для выхода): ")
                
                # Дополнительная удобная команда для штатного выхода из консоли
                if user_input.strip().lower() == "0":
                    print("Программа успешно завершена.")
                    break

                # Условие задачи: Пробуем превратить введенный текст в целое число.
                # Если пользователь ввёл буквы (например, "триста"), этот шаг вызовет ошибку ValueError.
                amount = int(user_input)
                
                # Если преобразование прошло успешно, прибавляем сумму к текущему значению в базе
                db['total'] += amount
                
                # Выводим обновленную сумму расходов пользователю
                print(f"Текущие расходы: {db['total']} руб.")

            except ValueError:
                # Условие задачи: Если int() выкинул ошибку, перехватываем её
                # и выводим строго заданное текстовое сообщение.
                print("Вы ввели неправильное значение")
                # Цикл не прерывается, программа перейдет на следующую итерацию и снова спросит сумму.
                
            except KeyboardInterrupt:
                # Условие задачи: Перехватываем нажатие Ctrl+C или кнопки Stop в IDE.
                # Выходим из цикла, чтобы программа завершилась красиво, без вывода пугающих системных ошибок.
                print("\n[INFO] Работа программы прервана пользователем.")
                break

# Проверяем, что файл запущен напрямую, а не импортирован как модуль
if __name__ == '__main__':
    manage_expenses()
