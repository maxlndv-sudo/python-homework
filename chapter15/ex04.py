# Ход решения
# 1. Повторное использование классов: Мы берем классы Point, Rectangle и Circle
# 2. Функция draw_rect(t, rect)
# известна стартовая точка — левый нижний угол прямоугольника rect.corner
# Первым делом нужно переместить черепашку в эту точку. Чтобы она не оставляла лишних линий при перемещении, мы поднимаем перо функцией t.up() и опускаем его t.down() уже на месте
# Далее в цикле рисуем стороны: дважды повторяем движение "вперед на ширину, поворот налево на 90 градусов, вперед на высоту, поворот налево на 90 градусов".
# 3. Функции draw_circle(t, circle)
# У встроенного объекта turtle есть готовый метод t.circle(radius). Но по умолчанию черепашка начинает рисовать круг от своей текущей позиции, 
# и эта позиция становится самой нижней точкой окружности, а не центром.
# Чтобы нарисовать круг с заданным центром circle.center, 
# нужно переместить черепашку в правильную стартовую точку: ровно на величину радиуса ниже центра (center.x, center.y - radius).
# После этого вызывается t.circle(circle.radius)

import turtle

# --- Копируем структуру классов из прошлых шагов ---

class Point:
    """Представление точки в двумерном пространстве."""
    def __init__(self, x=0.0, y=0.0):
        self.x = x
        self.y = y

class Rectangle:
    """Определяет прямоугольник с атрибутами width, height и corner (Point)."""
    def __init__(self, width=0.0, height=0.0, corner=None):
        self.width = width
        self.height = height
        self.corner = corner if corner is not None else Point()

class Circle:
    """Определяет круг с атрибутами center (Point) и radius."""
    def __init__(self, center=None, radius=0.0):
        self.center = center if center is not None else Point()
        self.radius = radius


# --- Функции рисования для Упражнения 15.4 ---

def draw_rect(t: turtle.Turtle, rect: Rectangle):
    """Принимает объект Turtle и Rectangle и рисует прямоугольник."""
    # Перемещаем черепашку в левый нижний угол без рисования линии
    t.up()
    t.goto(rect.corner.x, rect.corner.y)
    t.down()
    
    # Рисуем 4 стороны прямоугольника
    for _ in range(2):
        t.forward(rect.width)   # Движение по ширине (вправо)
        t.left(90)              # Поворот вверх
        t.forward(rect.height)  # Движение по высоте (вверх)
        t.left(90)              # Поворот влево


def draw_circle(t: turtle.Turtle, circle: Circle):
    """Принимает объект Turtle и Circle и рисует круг."""
    # Вычисляем стартовую точку (на радиус ниже центра круга)
    start_x = circle.center.x
    start_y = circle.center.y - circle.radius
    
    # Перемещаем черепашку на старт без рисования линии
    t.up()
    t.goto(start_x, start_y)
    t.down()
    
    # Рисуем окружность заданного радиуса
    t.circle(circle.radius)


# --- Демонстрация работы программы ---
if __name__ == '__main__':
    # Инициализируем графическое окно и исполнителя-черепашку
    window = turtle.Screen()
    window.title("Отрисовка геометрических фигур (Упражнение 15.4)")
    
    bob = turtle.Turtle()
    bob.speed(3)  # Задаем умеренную скорость анимации
    
    # 1. Создаем и рисуем прямоугольник
    box = Rectangle()
    box.width = 150.0
    box.height = 100.0
    box.corner = Point(-200, -50) # Разместим в левой части экрана
    
    bob.color("blue") # Прямоугольник будет синим
    draw_rect(bob, box)
    
    # 2. Создаем и рисуем круг (из Упражнения 15.3 с центром 150, 100 и r=75)
    my_circle = Circle(Point(150, 50), 75) # Разместим в правой части экрана
    
    bob.color("red") # Круг будет красным
    draw_circle(bob, my_circle)
    
    # Скрываем черепашку по окончании рисования
    bob.hideturtle()
    
    print("Рисование завершено. Закройте графическое окно для выхода.")
    window.mainloop() # Оставляем окно открытым

