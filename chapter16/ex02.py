# Нaпишите логическую функцию is_after, которая принимает два объекта Time (t1 и t2) и возвращает True, если t1 следует хронологически после t2, и False в противном случае. 
# Усложнение: не используйте оператор if

def is_after(t1, t2):
    """Возвращает True, если t1 строго позже, чем t2 (без использования if)."""
    # Сравниваем кортежи: Python сравнивает их поэлементно (часы, затем минуты, затем секунды)
    return (t1.hour, t1.minute, t1.second) > (t2.hour, t2.minute, t2.second)

# Ход решения
# В Python операции сравнения (например, >, <) над кортежами (tuple) работают поэлементно, слева направо.
# Мы упаковываем атрибуты времени в кортежи: (t1.hour, t1.minute, t1.second).

# Сначала он сравнивает часы. Если t1.hour > t2.hour, то первый кортеж сразу признается большим, и сравнение прекращается.
# Если часы равны, Python переходит ко второму элементу — минутам.
# Если и минуты равны, он сравнивает секунды.

# # Выражение (...) > (...) само по себе является логическим. 
  # Оно вычисляется и сразу возвращает готовый результат: либо True, либо False. Нам остается только написать return.
